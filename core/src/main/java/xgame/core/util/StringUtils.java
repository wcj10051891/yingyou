    private static final long oneHourMills = TimeUnit.HOURS.toMillis(1);
    private static final long oneMinuteMills = TimeUnit.MINUTES.toMillis(1);
    private static final long oneSecondMills = TimeUnit.SECONDS.toMillis(1);
    public static String formatTime(long mills) {
    	long hour = mills / oneHourMills;
    	long _minutes = mills % oneHourMills;
    	long minutes = _minutes / oneMinuteMills;
    	long _seconds = _minutes % oneMinuteMills;
    	long seconds = _seconds / oneSecondMills;
    	long millss = _seconds % oneSecondMills;
    	StringBuilder result = new StringBuilder();
    	if(hour > 0)
    		result.append(hour).append("小时");
    	if(minutes > 0)
    		result.append(minutes).append("分");
    	if(seconds > 0)
    		result.append(seconds).append("秒");
    	if(millss > 0)
    		result.append(millss).append("毫秒");
    	return result.toString();
    }
